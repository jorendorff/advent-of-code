from lib.advent import *
from lib.grid import Grid

def life_cycle(start):
    def transition(point, value):
        if value == 'L':
            if grid.count_visible(point, '#') == 0:
                value = '#'
        elif value == '#' and grid.count_visible(point, '#') >= 5:
            value = 'L'
        return value

    grid = Grid(start)
    return grid.cycle(transition)

def solve(start):
    c = life_cycle(start)
    if len(c.cycle) != 1:
        raise ValueError("oscillation detected")
    [s] = c.cycle
    return s.count('#')


